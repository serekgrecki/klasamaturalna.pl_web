<h2>Przebieg części ustnej egzaminu maturalnego z j. polskiego:</h2><br/><br/>
        </b>
Egzamin maturalny z j. polskiego w części ustnej sprawdza umiejętność tworzenia wypowiedzi na określony temat, inspirowanej tekstem kultury, zgodnej z zasadami poprawności językowej, logiki i retoryki.<br/><br/></center>

Czynności wstępne:<br/>
-Przewodniczący komisji egzaminacyjnej układa na stole zadania w kolejności od najmniejszego do największego numeru. Do losowania dla pierwszego zdającego wykłada na stół zadania z trzema pierwszymi numerami.<br/>
-Zdający, po okazaniu dokumentu stwierdzającego tożsamość, wchodzi do sali egzaminacyjnej wg ustalonej kolejności.
-W sali może przebywać dwóch zdających – jeden przygotowujący się i udzielający odpowiedzi.<br/>
<br/><br/>
Losowanie zadania:<br/>
-Maturzysta losuje jedno zadanie spośród zadań wyłożonych na stole i informuje komisję o numerze wylosowanego zadania.<br/>
- po wylosowaniu zadania przez zdającego, do pozostałych na stole dwóch zadań dodaje się zadanie oznaczone kolejnym numerem – tak aby na stole pozostawały zawsze trzy zadania, spośród których losuje kolejny zdający.<br/>
-Zdający przygotowuje się do egzaminu (15 minut – może być krócej ale nie dłużej)<br/>
-Po zakończeniu tego procesu (przygotowania) pierwszy zdający przechodzi do stolika, przy którym odbywa się egzamin. <br/>Jednak zanim rozpocznie swoją wypowiedz, do sali wchodzi następny zdający, który losuje jedno z trzech zadań i przechodzi do stolika, przy którym się przygotowuje 15 minut).<br/><br/>

Przebieg egzaminu:<br/>
-Rozpoczyna się egzamin – pierwsza osoba (po 15 min. przygotowania) odpowiada przed komisją, a druga się wówczas przygotowuje do swojej wypowiedzi.<br/>
-Zdający nie może robić notatek na wydruku zadania. Notatki zapisuje wyłącznie na kartkach z pieczątką szkoły, które również po egzaminie zostawia komisji.<br/>
-Zdający podchodzi do wypowiedzi z wydrukiem zadania oraz z własnymi notatkami.<br/>
-Egzamin trwa ok. 15 minut i składa się z wypowiedzi monologowej oraz rozmowy z komisją egzaminacyjną.<br/>
<br/><br/>
Wypowiedź monologowa:<br/>
-Trwa ok. 10 minut (zdający może wygłosić swoją wypowiedź w czasie nieco krótszym ale nie może przekroczyć tego czasu)<br/>
-Wypowiedź monologowa nie może być przerywana przez egzaminatorów (z wyjątkiem sytuacji, kiedy upłynął czas na nią przeznaczony)<br/><br/>

Rozmowa z komisją egzaminacyjną:<br/>
-Trwa ok. 5 minut<br/>
-Może dotyczyć zagadnienia określonego w poleceniu, tekstu kultury dołączonego do polecenia oraz treści i tekstów kultury przywołanych w wypowiedzi zdającego.<br/>
-Rozmowa nie może polegać na odpytaniu zdającego, pytania muszą być powiązane z problemem określonym w temacie.<br/>
- W czasie trwania egzaminu zdający nie może korzystać ze słowników i innych pomocy<br/>
-W czasie trwania części ustnej egzaminu maturalnego zdającym nie udziela się żadnych wyjaśnień dotyczących zadania egzaminacyjnego.<br/><br/>

Ocenianie<br/>
-Ocenie podlegają wszystkie elementy egzaminu, zgodnie z obowiązującymi kryteriami oceniania. Członkowie komisji indywidualnie oceniają każdego zdającego w trakcie odpowiedzi i nanoszą je na kartę indywidualnej oceny.<br/>
-Po przeprowadzeniu egzaminu dla grupy 5 zdających, komisja ustala liczbę punktów przyznanych każdemu zdającemu z tej grupy oraz przekazuje zdającym informacje o przyznanej punktacji (indywidualnie na karteczce)<br/>
-Podczas ustalania liczby punktów w sali nie mogą przebywać zdający maturzyści.<br/>
-Po przekazaniu zdającym – grupie 5 osób- informacji o ilości zdobytych punktów – kończy się dla nich część egzaminacyjna. Wówczas przewodniczący komisji egzaminacyjnej usuwa ze stołu niewylosowane zadania i wykłada na stół trzy nowe (kolejno numerowane) zadania do losowania dla następnej grupy 5 osób….  I cała procedura rozpoczyna się na nowo….<br/><br/>
